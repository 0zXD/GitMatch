export function getIssuesForSkills(skills: string[]): GitHubIssue[] {
  if (skills.length === 0) return mockIssues;

  // Filter issues based on selected skills
  return mockIssues.filter((issue) => {
    const issueSkills = [
      issue.language,
      ...issue.labels,
      issue.repository.split("/")[0],
    ].map((s) => s.toLowerCase());

    return skills.some((skill) => {
      const skillLower = skill.toLowerCase();
      return issueSkills.some((issueSkill) => issueSkill.includes(skillLower) || skillLower.includes(issueSkill));
    });
  });
}
